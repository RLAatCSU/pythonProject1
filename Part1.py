#Write a program where a user enters two numbers,
#then add the two numbers and display the result,
#then subtract the two numbers and display the result.

num1 = float(input("Input first number "))                      #User inputs the first number, convert to float.
num2 = float(input("Input second number "))                     #User inputs the second number, convert to float.
print("The first number + the second number =", num1 + num2)    #Add both numbers and display the result.
print("The first number - the second number =", num1 - num2)    #Subtract both numbers and display the result.