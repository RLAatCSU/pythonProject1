#Write a program where a user enters two numbers,
#then multiply the two numbers and display the result,
#then divide the two numbers and display the result.

num1 = float(input("Input first number "))                      #User inputs the first number, convert to float.
num2 = float(input("Input second number "))                     #User inputs the second number, convert to float.
print("The first number * the second number =", num1 * num2)    #Multiply both numbers and display the result.
print("The first number / the second number =", num1 / num2)    #Divide both numbers and display the result.